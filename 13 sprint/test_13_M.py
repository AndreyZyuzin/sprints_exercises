import pytest
from e13_M import get_median_slow, get_median


class Test_13_M:
    """Тестируем задачу."""

    @pytest.mark.parametrize(
        'array1, array2',
        [([0, 0, 0, 1, 3, 3, 5, 10], [4, 4, 5, 7, 7, 7, 8, 9, 9, 10]),
         ([1, 2, 3], [4, 5, 6]),
         ([1, 3], [2]),
         ([2], [1, 3]),
         ([1, 6, 9], [0, 3, 3, 3]),
         ([1, 6, 9], [10, 13, 13, 13]),
         ([1, 6, 9], [10, 13, 13]),
         ([1, 6, 9], [10, 13, 13, 15, 15]),
         ([11, 16, 19], [0, 3, 3]),
         ([8, 9, 11, 13, 15, 17, 25, 33, 34, 34, 37, 43, 45, 49, 51, 53, 58, 63, 63, 64, 73, 75, 78, 84, 92, 95, 96, 99],
          [0, 1, 2, 2, 3, 5, 6, 7, 8, 11, 11, 13, 16, 16, 17, 18, 19, 19, 19, 19, 20, 21, 21, 22, 25, 26, 28, 30, 30, 32, 32, 33, 34, 40, 40, 42, 42, 44, 46, 48, 49, 49, 50, 52, 53, 55, 56, 57, 58, 65, 68, 69, 69, 69, 70, 71, 73, 73, 73, 74, 75, 77, 80, 82, 84, 86, 88, 89, 92]),
         ([1, 6, 9], [10, 13, 13, 15, 15]),
         ([1, 6, 9], [10, 13, 13, 15, 15]),
         ([1, 6, 9], [10, 13, 13, 15, 15]),
         ([1, 6, 9], [10, 13, 13, 15, 15]),
         ([1, 6, 9], [10, 13, 13, 15, 15]),
         ])
    def test_merge(self, array1, array2):
        """Проверка правильного результата."""
        result_slow = get_median_slow(array1, array2)
        result_fast = get_median(array1, array2)
        assert result_fast == result_slow, (
            f'Массивы {array1} и {array2}: '
            f'медиана - {result_fast}, а не {result_slow}.')
